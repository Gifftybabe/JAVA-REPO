// Using 'var' keyword
var x = 5;
var y = 6;
var z = x + y;
console.log(z);

//Using 'let' keyword
let n = 5;
let p = 6;
let w = n + p;
console.log(w);


//Storing as undeclared variables.
a = 5;
b = 6;
c = a + y;
console.log(c);


//Using 'const' keyword
const price1 = 5;
const price2 = 6;
let total = price1 + price2;




